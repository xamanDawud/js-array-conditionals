var num1 = 23;
var num2 = 55;

var name1 = "xaman";
var name2 = "mohona";

// Comparison Operator
console.log(num1 == num2);
console.log(num1 === num2);
console.log(num1 != num2);
console.log(num1 !== num2);
console.log(num1 > num2);
console.log(num1 < num2);
console.log(num1 >= num2);
console.log(num1 <= num2);

// comparison and - or operator
console.log(num1 >= num2 && name1 == name2);
console.log(num1 >= num2 || name1 == name2);
